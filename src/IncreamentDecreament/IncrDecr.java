package IncreamentDecreament;

public class IncrDecr {


	public static void main(String[] args) {
	int a ;
	int b ;

	for (a = 0; a <= 5; a++) {
	System.out.println("the a++ is :" + a++);
	System.out.println(a);
	for (b = 0; b <= 5; ++b) {
	System.out.println("the ++a is :" + ++b);
	System.out.println(b);
	}
	}

	}

	}

