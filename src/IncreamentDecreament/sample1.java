package IncreamentDecreament;

public class sample1 {
	
	public static void main(String[] args) {
		
	
		int a = 10, b = 10;
		int c = a++ + b++ ;
	{
		int a1 = 10, b1=10;
		int c1 = a1++ + ++b1;
	 {

        int a2 = 10 , b2 = 10;
        int c2= ++a2 + b2++;
	{
		int a3=10,b3=10;
		int c3= ++a3 + ++b3;
		
		System.out.println(c);
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		
	}}}}}