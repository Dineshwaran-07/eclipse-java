package StringsLearning;

public class StringsSam {
	
	public static void main(String[] args) {
		
		String S1 = "Selenium Java";
		String S2 = "Automation Test";
		   

		System.out.println(S2.length());                        //Total no of characters in the string
		System.out.println(S1.equals(S2));                      //Compare two strings
		System.out.println(S1.equalsIgnoreCase(S2));            //This will compare two strings ignoring (DOUBT)

	}

}
